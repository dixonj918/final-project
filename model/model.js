export function changePageContent(pageID){
    let pageName = pageID + "Content";
    $("#app").html(eval(pageName));
    
    if(pageID == "home"){
      $("nav").removeClass("aboutBlack");
      $("nav #links").removeClass("aboutLinks");
      $("nav #logo").addClass("logo");
      $("nav #logo").removeClass("aboutLogo");
    }
    else{
      $("nav").addClass("aboutBlack");
      $("nav #links").addClass("aboutLinks");
      $("nav #logo").removeClass("logo");
      $("nav #logo").addClass("aboutLogo");

    }

}